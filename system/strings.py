string = {'en': en, 'ru': ru}

def center_2strings(conwidth, str1, str2):
	return print(str1 + (' ' * (conwidth // 2 - len(str1)) + ': ' + str2))

def colored_str(text, color = None):
	if not color:
		color = colorama.Back.GREEN
	return print(color + colorama.Fore.BLACK + text + ' ' * (10 - len(text)) + colorama.Style.RESET_ALL)

def console(config):
	global colorama
	s = string[config['lang']]
	from system.Lib import colorama, shutil
	conwidth = shutil.get_terminal_size().columns
	print((colorama.Back.BLACK + colorama.Fore.GREEN + s.xcoder % config['version'] + colorama.Style.RESET_ALL).center(conwidth + 14))
	print('github.com/MasterDevX/XCoder'.center(conwidth))
	print(conwidth * '-')
	colored_str(s.sc)
	center_2strings(conwidth, ' 1   ' + s.dsc, s.dsc_desc)
	center_2strings(conwidth, ' 2   ' + s.esc, s.esc_desc)
	center_2strings(conwidth, ' 3   ' + s.d1sc, s.expiremental)
	center_2strings(conwidth, ' 4   ' + s.e1sc, s.expiremental)
	print(conwidth * '-')
	colored_str(s.oth)
	center_2strings(conwidth, ' 101 ' + s.chkupd, s.version % config['version'])
	center_2strings(conwidth, ' 102 ' + s.reinit, s.reinit_desc)
	center_2strings(conwidth, ' 103 ' + s.relang, s.relang_desc % config['lang'])
	center_2strings(conwidth, ' 104 ' + s.clean_dirs, s.clean_dirs_desc)
	print(' 105 ' + s.exit)
	print(conwidth * '-')
	choice = input(s.choice)
	print(conwidth * '-')
	return choice



